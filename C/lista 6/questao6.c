/******************************************************************************

Escreva um programa em C para imprimir os números de 1 a 10 em ordem crescente.

*******************************************************************************/
#include <stdio.h>

int main()
{   
    //fazendo laço para realizar a leitura de ordem crescente
    for (int i = 1; i < 11; i++) {
        printf("%d\n", i);
    }
    return 0;
}